<script>
import commonMixin from '../base/mixins/common.js'
import { createSize } from '../base/factory.js'
import getMapMethod from '../base/methodMap.js';
import { deleteEmptyKey } from '../base/util.js'
import { getConfig } from '@vue-baidu-map-3x/utils'

export default {
  name: 'bm-navigation',
  render() { },
  mixins: [commonMixin('control')],
  props: {
    anchor: {
      type: String
    },
    offset: {
      type: Object
    },
    type: {
      type: String
    },
    showZoomInfo: {
      type: Boolean
    },
    enableGeolocation: {
      type: Boolean,
      default: false
    }
  },
  watch: {
    anchor() {
      this.reload()
    },
    offset() {
      this.reload()
    },
    type() {
      this.reload()
    },
    showZoomInfo() {
      this.reload()
    }
  },
  methods: {
    load() {
      const { BMap, map, anchor, offset, type, showZoomInfo, enableGeolocation } = this
      let navigationOption = {
        anchor: window[anchor],
        offset: offset && createSize(BMap, offset),
        type: window[type],
        showZoomInfo,
        enableGeolocation
      };
      deleteEmptyKey(navigationOption);
      this.originInstance = new BMap[getMapMethod('NavigationControl')](navigationOption)
      map.addControl(this.originInstance)
    }
  }
}
</script>
